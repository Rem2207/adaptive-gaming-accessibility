class Translator:
    """Simple dictionary-based translator for UI and user-facing messages."""

    def __init__(self, language: str = DEFAULT_LANGUAGE) -> None:
        self.language = language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE

    def set_language(self, language: str) -> None:
        self.language = language if language in SUPPORTED_LANGUAGES else DEFAULT_LANGUAGE

    def text(self, key: str, **kwargs: object) -> str:
        template = TRANSLATIONS.get(self.language, TRANSLATIONS[DEFAULT_LANGUAGE]).get(
            key,
            TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key),
        )
        return template.format(**kwargs) if kwargs else template



